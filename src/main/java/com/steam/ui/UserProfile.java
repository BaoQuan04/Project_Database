    private void setScaleImage(JLabel objectName, String path){
        Icon oldIcon = objectName.getIcon();
        if (oldIcon instanceof ImageIcon) {
            Image oldImg = ((ImageIcon) oldIcon).getImage();
            if (oldImg != null) {
                oldImg.flush(); // Gi·∫£i ph√≥ng b·ªô nh·ªõ c·ªßa ·∫£nh c≈©
            }
        }
        objectName.setIcon(null);
        ImageIcon icon = new ImageIcon(getClass().getResource(path));
        // L·∫•y k√≠ch th∆∞·ªõc th·ª±c t·∫ø c·ªßa JLabel
        
        int width = objectName.getWidth();
        int height = objectName.getHeight();

        if (width == 0){
            width = 50;
        }
        if (height == 0){
            height= 50;
        }
        // Co ·∫£nh l·∫°i cho v·ª´a khung
        Image img = icon.getImage().getScaledInstance(width, height, Image.SCALE_SMOOTH);
        ImageIcon scaledIcon = new ImageIcon(img);
        scaledIcon.setDescription(path); // üîπ L∆∞u l·∫°i ƒë∆∞·ªùng d·∫´n
        objectName.setIcon(null);
        objectName.setIcon(scaledIcon);
    
    }
    
